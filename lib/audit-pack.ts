function generateKnownLimitations(): string {
  return `# Known Limitations

> This document lists known limitations of the automated analysis and transformation engine.

## Static Analysis Constraints

- **Dynamic ABAP calls** (e.g., \`CALL FUNCTION func_name\` with variable names) cannot be fully resolved statically.
- **Dynpro / Screen Flows** are not 1:1 cloud-capable. UI5/Fiori migration requires separate design work.
- **Batch Input scenarios** require manual process redesign — they cannot be automatically converted.
- **Unreleased APIs** detected during mapping must not be used in production without SAP confirmation.

## Data Access Constraints

- **Direct table write operations** (INSERT, UPDATE, DELETE on standard tables) require explicit architecture approval.
- **Custom table dependencies** need manual classification (operational data vs. configuration vs. master data).
- **Cross-system RFC/IDoc dependencies** are flagged but not automatically migrated.

## Code Quality Constraints

- **Generated code is a draft** — it is not a production-approved deliverable.
- **Business semantics** may be misinterpreted without domain expert review.
- **Test coverage** is estimated, not measured. ABAP-Unit / Node.js tests must be executed in a real sandbox.
- **Performance characteristics** (e.g., mass data processing, parallel execution) are not validated.

## Compliance Constraints

- This analysis does not constitute a formal security audit.
- Data protection classification (personal data, financial data) requires a dedicated DPO review.
- Tenant connections are restricted to sandbox systems — production access is not supported.

---

*Generated by Clean-Core.io ${APP_VERSION} on ${new Date().toISOString()}*
`;
}

/**
 * Generates a Compliance Audit Pack as a ZIP blob.
 *
 * The pack contains only non-sensitive data: hashes, timestamps,
 * architecture decisions, and model metadata. No secrets, no
 * credentials, no unmaksed tenant URLs.
 */
export async function generateAuditPack(project: Project): Promise<Blob> {
  const zip = new JSZip();

  zip.file('00-executive-summary.md', generateExecutiveSummary(project));
  zip.file('00-executive-summary.doc', generateExecutiveSummaryDoc(project));

  zip.file('01-input-fingerprint.json', JSON.stringify(
    project.auditMetadata?.inputFingerprint || { note: 'No fingerprint available — analysis may predate v1.10.0' },
    null, 2
  ));

  zip.file('02-decision-record.json', JSON.stringify(
    generateDecisionRecord(project),
    null, 2
  ));

  zip.file('03-findings.csv', generateFindingsCsv(project));

  zip.file('04-model-card.md', generateModelCard(project));

  zip.file('05-known-limitations.md', generateKnownLimitations());

  return zip.generateAsync({ type: 'blob' });
}
