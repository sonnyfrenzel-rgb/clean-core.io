/**
 * Compliance Audit Pack Generator (v1.10.0)
 *
 * Generates an exportable ZIP containing structured evidence for
 * architecture governance reviews, compliance audits, and enterprise
 * decision documentation.
 *
 * Security: This module never includes secrets, credentials, API keys,
 * or unmaksed tenant URLs in the export. Only hashes, timestamps,
 * model identifiers, and architecture decisions are included.
 */

import JSZip from 'jszip';
import type { Project } from '@/lib/types';
import { APP_VERSION, APP_RELEASE_DATE } from '@/lib/version';

const ARCH_LABELS: Record<string, string> = {
  rap: 'In-App ABAP Cloud (RAP)',
  cap: 'Side-by-Side BTP (CAP)',
  integration: 'SAP Integration Suite',
  event: 'SAP Event Mesh',
  retire: 'Retire / Decommission',
};

function generateExecutiveSummary(project: Project): string {
  const fp = project.auditMetadata?.inputFingerprint;
  const mc = project.auditMetadata?.modelCard;
  const arch = project.targetArchitecture ? ARCH_LABELS[project.targetArchitecture] || project.targetArchitecture : 'Not determined';
  const approved = project.approvedByArchitect ? '✓ Approved' : '○ Pending';
  const approver = project.approvedBy ? ` by ${project.approvedBy}` : '';
  const signOffDate = project.architectSignOffAt
    ? new Date(project.architectSignOffAt as string).toISOString()
    : '—';

  return `# Compliance Audit Pack — Executive Summary

> Generated by Clean-Core.io ${APP_VERSION} on ${new Date().toISOString()}
> This document is a machine-generated draft for review. It does not replace a formal architecture sign-off.

---

## Project Overview

| Field | Value |
|---|---|
| Project Name | ${project.name} |
| Analysis Status | ${project.status || 'unknown'} |
| Clean Core Score | ${project.cleanCoreScore ?? '—'}/100 |
| Complexity Score | ${project.complexityScore ?? '—'}/100 |
| Criticality Score | ${project.criticalityScore ?? '—'}/100 |
| Target Architecture | ${arch} |
| Architect Approval | ${approved}${approver} |
| Sign-Off Date | ${signOffDate} |
| Extensibility Route | ${project.extensibilityRoute || '—'} |

## Input Summary

| Field | Value |
|---|---|
| File Name | ${fp?.fileName || '—'} |
| Object Type | ${fp?.objectType || '—'} |
| Lines of Code | ${fp?.lineCount ?? '—'} |
| Input Size | ${fp?.byteSize ? `${(fp.byteSize / 1024).toFixed(1)} KB` : '—'} |
| SHA-256 Fingerprint | \`${fp?.sha256 || '—'}\` |
| Upload Timestamp | ${fp?.uploadedAt || '—'} |

## Engine Metadata

| Field | Value |
|---|---|
| Platform Version | ${mc?.engineVersion || APP_VERSION} |
| Model Provider | ${mc?.provider || 'google-gemini'} |
| Model | ${mc?.model || '—'} |
| BYOK Used | ${mc?.byokUsed ? 'Yes' : 'No'} |
| Analysis Timestamp | ${mc?.analysisTimestamp || '—'} |
| Design Timestamp | ${mc?.designTimestamp || '—'} |
| Transformation Timestamp | ${mc?.transformationTimestamp || '—'} |

---

## Audit Pack Disclaimer

This audit pack documents an automated modernization analysis. The results represent a technical and architectural decision aid. They do not replace a formal architecture sign-off, a data protection review, or a production SAP acceptance test.

Generated code is a draft for review — not a production-ready deliverable.
`;
}

